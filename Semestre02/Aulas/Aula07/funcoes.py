def calcsoma (n1: float, n2: float) -> float:
    return n1 + n2

def calcsubtracao (n1: float, n2: float) -> float:
    return n1 - n2

def calcmultiplicacao (n1: float, n2: float) -> float:
    return n1 * n2

def calcdivisao (n1: float, n2: float) -> float:
    return n1 / n2 if n2 != 0 else "Erro de divisão por zero"
